class Q1_Student {

    private String name, no;

    // 생성자
    public Student(String name, String no) {
        this.name = name;
        this.no = no;
    }

    // getter, setter 생성
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }
}