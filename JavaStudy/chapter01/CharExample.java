// 문자 타입

public class CharExample {
    public static void main(String[] args){
        char c1 = '가';   // 문자 저장
        char c2 = 65;
        char c3 = 66;
        char c4 = 44032;  // 유니코드 직접 저장

        System.out.println("c1 : " + c1);
        System.out.println("c2 : " + c2);
        System.out.println("c3 : " + c3);
        System.out.println("c4 : " + c4);
    }
}